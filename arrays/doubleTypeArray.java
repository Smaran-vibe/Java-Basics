package arrays;

public class doubleTypeArray {
    public static void main(String[] args) {

        // Create double array
        double number[] = {3.5, 8.9, 1.6,-4.8};

        // Access array elements using the for loop
        for (int i=0 ; i<number.length ; i++) {
            System.out.println(number[i]);
        }
    }

}
